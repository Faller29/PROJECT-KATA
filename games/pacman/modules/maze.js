// Function to count dots in the maze
export function countDots(maze) {
    let count = 0;
    for (let y = 0; y < maze.length; y++) {
        for (let x = 0; x < maze[y].length; x++) {
            if (maze[y][x] === 2) {
                count++;
            }
        }
    }
    return count;
}

// Function to check if movement is possible in a direction
export function canMove(x, y, direction, maze, CELL_SIZE) {
    const gridX = Math.round(x / CELL_SIZE);
    const gridY = Math.round(y / CELL_SIZE);
    
    switch (direction) {
        case 'up':
            return gridY > 0 && maze[gridY - 1][gridX] !== 1;
        case 'down':
            return gridY < maze.length - 1 && maze[gridY + 1][gridX] !== 1;
        case 'left':
            return gridX > 0 && maze[gridY][gridX - 1] !== 1;
        case 'right':
            return gridX < maze[0].length - 1 && maze[gridY][gridX + 1] !== 1;
        default:
            return false;
    }
}
